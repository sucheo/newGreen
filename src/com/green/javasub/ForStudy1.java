package com.green.javasub;

public class ForStudy1 {
    public static void main(String[] args) {
        for(int i = 0; i<10; i++){
            System.out.printf("%s히히\n",i);
        }
        System.out.println("------------------");
        for (int i = 0; i < 5; i++) {
            System.out.println("hello");
        }
        System.out.println("-------");
        for (int i = 0; i < 8; i++) {
            System.out.println(i+1);
            
        }
        System.out.println("-------");
        for (int i = 15; i < 23; i++) {
            System.out.println(i-14);
        }
        System.out.println("-----");
        for (int i = 30; i > 25; i--) {
            System.out.println(i-25);
        }
    }
}
