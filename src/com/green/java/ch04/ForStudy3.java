package com.green.java.ch04;

public class ForStudy3 {
    public static void main(String[] args) {
        for(int i=0; i<=3; i++){
            for(int z=0; z<=5; z++){
                System.out.printf("i : %d\t z : %d\n",i,z);
            }
            for(int j=0; j<=5; j++){
                System.out.printf("i : %d\t j : %d\n",i,j);
            }
        }
    }
}
