package com.green.java.ch03;

public class OperatorStudy8 {
    public static void main(String[] args) {

        // 미만, 초과
        // 이하, 이상

        System.out.println("2 < 2 : " + (2 < 2)); // 미만
        System.out.println("2 <= 2 : " + (2 <= 2)); // 이하
        System.out.println("1 <= 2 : " + (1 <= 2)); //
        System.out.println("2 == 2 : " + (2 == 2)); //
        System.out.println("2 != 2 : " + (2 != 2)); // 두개가 다를때 true0
        System.out.println("2 > 2 : " + (2 > 2)); // 초과
        System.out.println("2 >= 2 : " + (2 >= 2)); // 이상
        System.out.println("1 >= 2 : " + (1 >= 2)); // 이상

    }
}